    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            // Ensure the connection is established
            if (conn == null || conn.isClosed()) {
                // Handle the case where the connection is not properly initialized
                JOptionPane.showMessageDialog(rootPane, "Database connection is not established.", "Connection Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Retrieve and validate user input
            
            String StdName = name.getText();
            String StdFName = stdfname.getText();
            String StdBlood = blood.getText();
            String StdPhone = stdphone.getText();
            String StdCity = stdcity.getText();
            String classText = stdclass.getText();
            int stdId = Integer.parseInt(id.getText());

            if (StdName.isEmpty() || StdFName.isEmpty() || StdBlood.isEmpty() || StdPhone.isEmpty() || StdCity.isEmpty() || classText.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Please fill in all fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int StdClass;
            try {
                StdClass = Integer.parseInt(classText);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Please enter a valid number for the class.", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Prepare and execute the SQL statement
            String sql = "UPDATE student SET stdName = ?, stdFName = ?, stdBlood = ?, stdPhone = ?, stdCity = ?, stdClass = ? WHERE id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(7, stdId);
            pstmt.setString(1, StdName);
            pstmt.setString(2, StdFName);
            pstmt.setString(3, StdBlood);
            pstmt.setString(4, StdPhone);
            pstmt.setString(5, StdCity);
            pstmt.setInt(6, StdClass);

            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Student data successfully updated!");

            // Close the PreparedStatement
            pstmt.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Enter all details properly!!", "Invalid Entry", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

