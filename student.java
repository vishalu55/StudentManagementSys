    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int stdId = Integer.parseInt(id.getText());
            String sql = "SELECT * FROM student where id='"+stdId+"'";
            res = stmt.executeQuery(sql);
            if(res.next()){
                name.setText(res.getString("stdName"));
                stdfname.setText(res.getString("stdFName"));
                stdcity.setText(res.getString("stdCity"));
                stdphone.setText(res.getString("stdPhone"));
                blood.setText(res.getString("stdBlood"));
                stdclass.setText(res.getString("stdClass"));
            }
            else{
                JOptionPane.showMessageDialog(null, "Record not found!!");
            }
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, "Record not found!! Tip-Enter stduent id correctly.");
    }
        
    }//GEN-LAST:event_jButton3ActionPerformed

